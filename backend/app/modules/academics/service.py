    async def generate_curriculum_report(self, db: AsyncSession, context: dict[str, Any]) -> tuple[str, list[dict[str, Any]]]:
        school_id = self._school(context)
        rows = await db.execute(select(CurriculumAuditEntry).where(CurriculumAuditEntry.school_id == school_id))
        audits = rows.scalars().all()

        by_programme: dict[str, list[CurriculumAuditEntry]] = defaultdict(list)
        for audit in audits:
            by_programme[audit.programme].append(audit)

        report_rows: list[dict[str, Any]] = []
        for programme, items in sorted(by_programme.items()):
            avg_alignment = round(sum(item.alignment_score for item in items) / max(1, len(items)), 1)
            needs_update = len([item for item in items if item.status in {CurriculumAuditStatus.NEEDS_UPDATE, CurriculumAuditStatus.NON_COMPLIANT}])
            report_rows.append(
                {
                    "programme": programme,
                    "entry_count": len(items),
                    "avg_alignment": avg_alignment,
                    "needs_update": needs_update,
                }
            )

        low_courses = sorted(audits, key=lambda x: float(x.alignment_score))[:5]
        top_gaps = [
            {
                "course_code": item.course_code,
                "course_name": item.course_name,
                "alignment_score": item.alignment_score,
                "gaps": item.gaps,
            }
            for item in low_courses
        ]

        reflection = f"Generated curriculum audit report for {school_id} with {len(report_rows)} programme summaries."
        return reflection, [
            {"status": "programme_rollup_created", "programme_summaries": report_rows},
            {"status": "annual_audit_report_ready", "top_gap_courses": top_gaps},
        ]

