    async def audit_syllabus(self, db: AsyncSession, context: dict[str, Any]) -> tuple[str, list[dict[str, Any]]]:
        school_id = self._school(context)
        await self.ensure_curriculum_seed(db, school_id)

        programme_filter = str(context.get("programme") or "").strip().lower()
        rows = await db.execute(select(CurriculumAuditEntry).where(CurriculumAuditEntry.school_id == school_id))
        audits = rows.scalars().all()

        if programme_filter:
            audits = [a for a in audits if programme_filter in a.programme.lower()]

        updated: list[dict[str, Any]] = []
        for audit in audits:
            gap = abs(audit.exam_frequency - audit.syllabus_weight)
            audit.alignment_score = round(max(35.0, min(98.0, (audit.industry_relevance + (100 - gap)) / 2)), 1)
            if audit.alignment_score >= 85:
                audit.status = CurriculumAuditStatus.ALIGNED
            elif audit.alignment_score >= 70:
                audit.status = CurriculumAuditStatus.REVIEWING
            else:
                audit.status = CurriculumAuditStatus.NEEDS_UPDATE

            gaps = []
            if gap > 25:
                gaps.append("Exam weight and syllabus weight are materially misaligned")
            if audit.industry_relevance < 65:
                gaps.append("Industry relevance is below target")
            if not gaps:
                gaps.append("No critical gaps detected")

            recommendations = [
                "Increase practical lab coverage for high-weight topics" if audit.industry_relevance < 75 else "Maintain current lab depth",
                "Revise assessment blueprint to match current syllabus weights",
            ]

            audit.gaps = gaps
            audit.recommendations = recommendations
            audit.last_audited_at = datetime.now(timezone.utc)
            updated.append(
                {
                    "course_code": audit.course_code,
                    "course_name": audit.course_name,
                    "alignment_score": audit.alignment_score,
                    "status": audit.status.value,
                    "gaps": gaps,
                }
            )

        await db.flush()
        reflection = f"Completed syllabus audit for {len(updated)} curriculum entries in {school_id}."
        return reflection, [
            {"status": "curriculum_scope_resolved", "entry_count": len(audits), "programme_filter": programme_filter or "all"},
            {"status": "syllabus_audit_completed", "updated_entries": updated[:25]},
        ]

    async def nep_compliance(self, db: AsyncSession, context: dict[str, Any]) -> tuple[str, list[dict[str, Any]]]:
        school_id = self._school(context)
        rows = await db.execute(select(CurriculumAuditEntry).where(CurriculumAuditEntry.school_id == school_id))
        audits = rows.scalars().all()

        total = max(1, len(audits))
        aligned = len([a for a in audits if a.alignment_score >= 80])
        multidisciplinary_readiness = round((aligned / total) * 100, 1)
        abc_readiness = round(sum(min(100.0, a.industry_relevance + 5.0) for a in audits) / total, 1)
        obe_documentation = round(sum(max(0.0, 100.0 - abs(a.exam_frequency - a.syllabus_weight)) for a in audits) / total, 1)

        score = round((multidisciplinary_readiness + abc_readiness + obe_documentation) / 3, 1)
        status = "compliant" if score >= 75 else "partial_compliance"

        reflection = f"NEP compliance assessment complete for {school_id} with score {score}."
        return reflection, [
            {
                "status": "nep_compliance_assessed",
                "overall_score": score,
                "status_label": status,
                "metrics": {
                    "multidisciplinary_readiness": multidisciplinary_readiness,
                    "academic_bank_readiness": abc_readiness,
                    "obe_documentation": obe_documentation,
                },
            }
        ]

