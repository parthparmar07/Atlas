    async def check_exam_clashes(self, db: AsyncSession, context: dict[str, Any]) -> tuple[str, list[dict[str, Any]]]:
        school_id = self._school(context)
        await self.ensure_exam_seed(db, school_id)

        rows = await db.execute(
            select(ExamSchedule).where(ExamSchedule.school_id == school_id).order_by(ExamSchedule.exam_date, ExamSchedule.start_time)
        )
        schedules = rows.scalars().all()

        hall_map: dict[tuple[str, str, str], list[ExamSchedule]] = defaultdict(list)
        section_map: dict[tuple[str, str, str], list[ExamSchedule]] = defaultdict(list)
        clashes: list[dict[str, Any]] = []

        for exam in schedules:
            day_key = exam.exam_date.date().isoformat()
            start = exam.start_time.strftime("%H:%M")
            end = exam.end_time.strftime("%H:%M")
            hall_map[(day_key, start, exam.hall_name)].append(exam)
            section_map[(day_key, start, exam.section)].append(exam)
            if exam.registered_students > exam.hall_capacity:
                clashes.append(
                    {
                        "type": "capacity_overflow",
                        "exam_id": exam.id,
                        "exam_name": exam.exam_name,
                        "hall": exam.hall_name,
                        "registered": exam.registered_students,
                        "capacity": exam.hall_capacity,
                        "severity": "critical",
                    }
                )

        for key, grouped in hall_map.items():
            if len(grouped) > 1:
                clashes.append(
                    {
                        "type": "hall_double_booking",
                        "day": key[0],
                        "start": key[1],
                        "hall": key[2],
                        "exam_ids": [g.id for g in grouped],
                        "severity": "critical",
                    }
                )

        for key, grouped in section_map.items():
            if len(grouped) > 1:
                clashes.append(
                    {
                        "type": "section_double_booking",
                        "day": key[0],
                        "start": key[1],
                        "section": key[2],
                        "exam_ids": [g.id for g in grouped],
                        "severity": "critical",
                    }
                )

        reflection = f"Exam clash audit complete for {school_id}: {len(clashes)} clash records detected."
        return reflection, [
            {"status": "exam_schedule_loaded", "records": len(schedules)},
            {"status": "exam_clash_report", "clashes": clashes, "clash_count": len(clashes)},
        ]

    async def optimize_exam_schedule(self, db: AsyncSession, context: dict[str, Any]) -> tuple[str, list[dict[str, Any]]]:
        school_id = self._school(context)
        rows = await db.execute(
            select(ExamSchedule)
            .where(ExamSchedule.school_id == school_id)
            .order_by(ExamSchedule.exam_date, ExamSchedule.start_time)
            .limit(100)
        )
        schedules = rows.scalars().all()

        by_day_section: dict[tuple[str, str], list[ExamSchedule]] = defaultdict(list)
        for exam in schedules:
            by_day_section[(exam.exam_date.date().isoformat(), exam.section)].append(exam)

        adjustments: list[dict[str, Any]] = []
        for (_day, _section), exams in by_day_section.items():
            if len(exams) <= 1:
                continue
            for idx, exam in enumerate(sorted(exams, key=lambda x: x.start_time)):
                if idx == 0:
                    continue
                old_day = exam.exam_date.date().isoformat()
                exam.exam_date = exam.exam_date + timedelta(days=idx)
                exam.start_time = exam.start_time + timedelta(days=idx)
                exam.end_time = exam.end_time + timedelta(days=idx)
                exam.status = ExamScheduleStatus.RESCHEDULED
                adjustments.append(
                    {
                        "exam_id": exam.id,
                        "exam_name": exam.exam_name,
                        "old_day": old_day,
                        "new_day": exam.exam_date.date().isoformat(),
                        "reason": "Reduced same-day section load",
                    }
                )

        for exam in schedules:
            if exam.registered_students <= exam.hall_capacity:
                continue
            original = exam.hall_name
            exam.hall_name = "Grand Hall"
            exam.hall_capacity = max(exam.registered_students + 10, 150)
            adjustments.append(
                {
                    "exam_id": exam.id,
                    "exam_name": exam.exam_name,
                    "old_hall": original,
                    "new_hall": exam.hall_name,
                    "reason": "Capacity overflow remediation",
                }
            )

        await db.flush()
        reflection = f"Optimized exam schedule for {school_id} with {len(adjustments)} adjustments."
        return reflection, [
            {"status": "optimization_inputs_loaded", "record_count": len(schedules)},
            {"status": "exam_optimization_completed", "adjustments": adjustments},
        ]

    async def generate_calendar(self, db: AsyncSession, context: dict[str, Any]) -> tuple[str, list[dict[str, Any]]]:
        school_id = self._school(context)
        await self.ensure_calendar_seed(db, school_id)

        start_raw = str(context.get("start_date") or "").strip()
        end_raw = str(context.get("end_date") or "").strip()
        today = datetime.now(timezone.utc)
        start = datetime.fromisoformat(start_raw).replace(tzinfo=timezone.utc) if start_raw else today.replace(month=7, day=1, hour=9, minute=0, second=0, microsecond=0)
        end = datetime.fromisoformat(end_raw).replace(tzinfo=timezone.utc) if end_raw else start + timedelta(days=150)

        holidays = self._as_list(context.get("holidays"))
        holiday_dates = set()
        for holiday in holidays:
            if isinstance(holiday, dict) and holiday.get("date"):
                holiday_dates.add(str(holiday["date"]))
            elif isinstance(holiday, str) and holiday:
                holiday_dates.add(holiday[:10])

        teaching_blocks = 0
        cursor = start
        generated: list[dict[str, Any]] = []
        while cursor <= end:
            if cursor.weekday() < 5:
                title = f"Teaching Day {cursor.strftime('%Y-%m-%d')}"
                event_type = CalendarEventType.TEACHING_DAY
                is_holiday = cursor.date().isoformat() in holiday_dates
                if is_holiday:
                    title = f"Holiday: {cursor.strftime('%Y-%m-%d')}"
                    event_type = CalendarEventType.HOLIDAY
                db.add(
                    AcademicCalendarEvent(
                        school_id=school_id,
                        title=title,
                        event_type=event_type,
                        start_at=cursor,
                        end_at=cursor + timedelta(hours=8),
                        owner="Academic Cell",
                        is_holiday=is_holiday,
                        is_locked=is_holiday,
                        metadata_json={"generated": True},
                    )
                )
                teaching_blocks += 0 if is_holiday else 1
                if len(generated) < 20:
                    generated.append({"date": cursor.date().isoformat(), "title": title, "event_type": event_type.value})
            cursor += timedelta(days=1)

        await db.flush()
        reflection = f"Generated calendar scaffold for {school_id} with {teaching_blocks} teaching days."
        return reflection, [
            {"status": "calendar_inputs_resolved", "start_date": start.date().isoformat(), "end_date": end.date().isoformat()},
            {"status": "calendar_generated", "teaching_days": teaching_blocks, "sample_events": generated},
        ]

    async def add_calendar_event(self, db: AsyncSession, context: dict[str, Any]) -> tuple[str, list[dict[str, Any]]]:
        school_id = self._school(context)
        event = context.get("event") if isinstance(context.get("event"), dict) else {}

        title = str(event.get("name") or context.get("event_name") or "New Academic Event").strip()
        event_type_raw = str(event.get("type") or context.get("event_type") or "event").strip().lower()
        start_raw = str(event.get("date") or context.get("date") or datetime.now(timezone.utc).date().isoformat())

        start = datetime.fromisoformat(start_raw).replace(tzinfo=timezone.utc)
        end = start + timedelta(hours=6)

        kind = CalendarEventType.EVENT
        if event_type_raw == "holiday":
            kind = CalendarEventType.HOLIDAY
        elif event_type_raw == "exam":
            kind = CalendarEventType.EXAM
        elif event_type_raw == "teaching_day":
            kind = CalendarEventType.TEACHING_DAY

        overlap_rows = await db.execute(
            select(AcademicCalendarEvent).where(
                AcademicCalendarEvent.school_id == school_id,
                AcademicCalendarEvent.start_at <= end,
                AcademicCalendarEvent.end_at >= start,
            )
        )
        overlaps = overlap_rows.scalars().all()

        item = AcademicCalendarEvent(
            school_id=school_id,
            title=title,
            event_type=kind,
            start_at=start,
            end_at=end,
            owner=str(context.get("owner") or "Academic Cell"),
            is_holiday=kind == CalendarEventType.HOLIDAY,
            is_locked=kind in {CalendarEventType.EXAM, CalendarEventType.HOLIDAY},
            metadata_json={"overlap_count": len(overlaps)},
        )
        db.add(item)
        await db.flush()

        reflection = f"Added calendar event '{title}' with {len(overlaps)} potential overlaps."
        return reflection, [
            {"status": "calendar_overlap_check", "overlap_count": len(overlaps), "overlap_ids": [o.id for o in overlaps[:10]]},
            {
                "status": "calendar_event_added",
                "event": {
                    "id": item.id,
                    "title": item.title,
                    "event_type": item.event_type.value,
                    "date": item.start_at.date().isoformat(),
                },
            },
        ]

    async def holiday_mapping(self, db: AsyncSession, context: dict[str, Any]) -> tuple[str, list[dict[str, Any]]]:
        school_id = self._school(context)
        await self.ensure_calendar_seed(db, school_id)

        rows = await db.execute(select(AcademicCalendarEvent).where(AcademicCalendarEvent.school_id == school_id))
        events = rows.scalars().all()

        holidays = [e for e in events if e.event_type == CalendarEventType.HOLIDAY or e.is_holiday]
        teaching = [e for e in events if e.event_type == CalendarEventType.TEACHING_DAY and not e.is_holiday]
        lost_days = len([h for h in holidays if h.start_at.weekday() < 5])
        effective_days = max(0, len(teaching) - lost_days)
        recommendations = []
        if lost_days > 8:
            recommendations.append("Add 2 compensatory Saturdays for each programme cluster.")
        if effective_days < 90:
            recommendations.append("Shift one assessment week to blended mode to recover teaching hours.")
        if not recommendations:
            recommendations.append("Current holiday distribution is within academic tolerance bands.")

        reflection = (
            f"Holiday mapping complete for {school_id}: {lost_days} weekday holidays, "
            f"{effective_days} effective teaching days."
        )
        return reflection, [
            {
                "status": "holiday_inventory_built",
                "holiday_count": len(holidays),
                "weekday_holiday_count": lost_days,
                "teaching_day_count": len(teaching),
            },
            {
                "status": "holiday_impact_assessed",
                "effective_teaching_days": effective_days,
                "recommendations": recommendations,
            },
        ]

